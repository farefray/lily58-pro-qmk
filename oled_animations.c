void render_master(void)
{
    if (get_current_wpm() > 15)
    {
        is_typing = true;
    }
    else
    {
        is_typing = false;
    }

    if (is_typing)
    {
        render_anim();
    }
    else
    {
        oled_clear();

        render_layer_state();
        // render_mod_status_gui_alt(get_mods() | get_oneshot_mods());
        // render_mod_status_ctrl_shift(get_mods() | get_oneshot_mods());
        render_space();
        render_logo();
        render_space();
    }
}

bool oled_task_user(void)
{
    if (is_keyboard_master())
    {
        render_master();
    }

    return false;
}
